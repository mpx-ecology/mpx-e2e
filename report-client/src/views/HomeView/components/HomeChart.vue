<script setup lang="ts">
import { onMounted, ref, watch } from 'vue'
// 引入 echarts 核心模块，核心模块提供了 echarts 使用必须要的接口。
import * as echarts from 'echarts/core';
// 引入柱状图图表，图表后缀都为 Chart
import { BarChart } from 'echarts/charts';
// 引入提示框，标题，直角坐标系，数据集，内置数据转换器组件，组件后缀都为 Component
import {
  TitleComponent,
  TooltipComponent,
  GridComponent,
  DatasetComponent,
  TransformComponent
} from 'echarts/components';
// 标签自动布局，全局过渡动画等特性
import { LabelLayout, UniversalTransition } from 'echarts/features';
// 引入 Canvas 渲染器，注意引入 CanvasRenderer 或者 SVGRenderer 是必须的一步
import { CanvasRenderer } from 'echarts/renderers';
echarts.use([
  TitleComponent,
  TooltipComponent,
  GridComponent,
  DatasetComponent,
  TransformComponent,
  BarChart,
  LabelLayout,
  UniversalTransition,
  CanvasRenderer
]);

type Props = {
  list: number[],
  file: string[]
}
// 使用 <script setup>
const props = defineProps<Props>()
const main = ref(null)
let myChart: echarts.ECharts

watch(props, () => {
  if (props.file.length || props.list.length) {
    myChart && myChart.setOption({
        xAxis: {
        type: 'category',
        data: props.file
      },
      series: [
        {
          data: props.list,
          type: 'bar'
        }
      ]
    });
  }
})

onMounted(() => {
  if (!main.value) return
  myChart = echarts.init(main.value);
  var option;
  option = {
    tooltip: {
      trigger: 'axis',
      axisPointer: {
        type: 'shadow'
      }
    },
    grid: {
      left: '3%',
      right: '4%',
      bottom: '3%',
      containLabel: true
    },
    xAxis: {
      type: 'category',
      data: props.file
    },
    yAxis: {
      type: 'value'
    },
    series: [
      {
        data: props.list,
        type: 'bar'
      }
    ]
  };

  option && myChart.setOption(option);
})

</script>

<template>
  <div ref="main" style="width: 800px; height: 300px;" id="main"></div>
</template>